package org.firstinspires.ftc.teamcode.Autonomous;

import androidx.annotation.NonNull;

import com.acmerobotics.dashboard.telemetry.TelemetryPacket;
import com.acmerobotics.roadrunner.Action;
import com.acmerobotics.roadrunner.PoseVelocity2d;
import com.acmerobotics.roadrunner.Vector2d;

import org.firstinspires.ftc.robotcore.external.Telemetry;
//import org.firstinspires.ftc.teamcode.OpModes.ColorReturn;


public class AddTele{
    Telemetry tele;
    String message;
    double num;

    public AddTele(String message, double num, Telemetry telerc) {
        this.tele = telerc;
        this.message = message;
        this.num = num;
    }
    public class addTele implements Action{
        @Override
        public boolean run(@NonNull TelemetryPacket telemetryPacket) {
            tele.addData(message, num);
            tele.update();
            return false;
        }
    }
    public Action addTele() {
        return new AddTele.addTele();
    }
}




