package org.firstinspires.ftc.teamcode.VisualColor.model;

public class ArmAction {
    double ArmAngle;
    double ClipAngle;
    double length;
    double GoToX;
    double GoToY;
    public ArmAction(double armAngle, double clipAngle, double length, double goToX, double goToY) {
        this.ArmAngle = armAngle;
        this.ClipAngle = clipAngle;
        this.length = length;
        this.GoToX = goToX;
        this.GoToY = goToY;
    }
}
